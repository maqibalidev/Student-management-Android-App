package com.example.rightschooldemoapp.Models_for_recycler;

public class Model_for_help {

    String title,detail;


    public Model_for_help(String title, String detail) {
        this.title = title;
        this.detail = detail;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
